#include <stdio.h>

int main(){
    int arr[3][3] = {1,2,3,4,5,6,7,8,9};
    // 3행 3열의 2차원 배열 선언 및 초기화
    // 1 2 3
    // 4 5 6
    // 7 8 9

    int* parr[2] = {arr[1], arr[2]};
    // 2개의 포인터 배열 선언 및 초기화
    // parr[0] -> arr[1] -> 4 5 6
    // parr[1] -> arr[2] -> 7 8 9

    printf("%d", parr[1][1] +  *(parr[1]+2) + **parr);
    // parr[1][1] -> arr[2][1] -> 8
    // *(parr[1]+2) -> arr[2][2] -> 9
    // **parr -> arr[1][0] -> 4
    // 출력 결과: 8 + 9 + 4 = 21
}